// ── page 3: sentiment & evidence ─────────────────────────────────────────────

function page3(doc, results) {
  doc.addPage();
  pageBase(doc);
  pageHeader(doc, results.run?.brand_name || '', 3);

  let y = 20;

  y = sectionHeading(doc, 'Sentiment Analysis', null, y);

  const sentiment = results.sentimentByBrand || [];
  const sentMap = Object.fromEntries(sentiment.map(s => [s.sentiment, s]));

  // Sentiment metric cards
  const sCW = (CW - 6) / 3;
  const sCards = [
    { label: 'Positive Sentiment', value: `${sentMap.positive?.pct || 0}%`, sub: `${sentMap.positive?.count || 0} responses`, color: C.green },
    { label: 'Neutral Sentiment',  value: `${sentMap.neutral?.pct || 0}%`,  sub: `${sentMap.neutral?.count || 0} responses`,  color: C.muted },
    { label: 'Negative Sentiment', value: `${sentMap.negative?.pct || 0}%`, sub: `${sentMap.negative?.count || 0} responses`, color: C.red },
  ];
  sCards.forEach((card, i) => {
    const x = M + i * (sCW + 3);
    doc.setFillColor(...C.card);
    doc.roundedRect(x, y, sCW, 22, 2, 2, 'F');
    doc.setFillColor(...card.color);
    doc.rect(x, y, sCW, 1.5, 'F');
    doc.setFontSize(18);
    doc.setTextColor(...card.color);
    doc.text(card.value, x + sCW / 2, y + 12, { align: 'center' });
    doc.setFontSize(7.5);
    doc.setTextColor(...C.white);
    doc.text(card.label, x + sCW / 2, y + 17.5, { align: 'center' });
    doc.setFontSize(6.5);
    doc.setTextColor(...C.dim);
    doc.text(card.sub, x + sCW / 2, y + 21.5, { align: 'center' });
  });
  y += 27;

  // Stacked sentiment bar
  const sentColors = { positive: C.green, neutral: C.muted, negative: C.red };
  let barX = M;
  ['negative', 'neutral', 'positive'].forEach(k => {
    const s = sentMap[k];
    if (!s) return;
    const bw = (s.pct / 100) * CW;
    doc.setFillColor(...sentColors[k]);
    doc.rect(barX, y, Math.max(bw - 0.5, 0.5), 7, 'F');
    barX += bw;
  });
  y += 9;
  let legendX = M;
  ['negative', 'neutral', 'positive'].forEach(k => {
    const s = sentMap[k];
    if (!s) return;
    doc.setFillColor(...sentColors[k]);
    doc.rect(legendX, y - 1, 3, 3, 'F');
    doc.setFontSize(7.5);
    doc.setTextColor(...C.muted);
    const lbl = `${k.charAt(0).toUpperCase() + k.slice(1)}: ${s.pct}%`;
    doc.text(lbl, legendX + 5, y + 2);
    legendX += doc.getTextWidth(lbl) + 16;
  });
  y += 12;

  // Representative positive responses
  const responses = (results.sampleResponses || [])
    .filter(r => r.brand_mentioned === 1)
    .sort((a, b) => (b.sentiment === 'positive' ? 1 : 0) - (a.sentiment === 'positive' ? 1 : 0))
    .slice(0, 3);

  if (responses.length) {
    y = sectionHeading(doc, 'Representative AI Responses', `Responses where ${results.run?.brand_name} was mentioned`, y);

    responses.forEach(resp => {
      if (y > 268) return;

      const modelLabel = resp.model === 'gpt-4o' ? 'GPT-4o Mini' : resp.model === 'claude-3-5-sonnet' ? 'Claude Haiku' : resp.model;
      const modelColor = resp.model === 'gpt-4o' ? C.green : C.purple;
      const sentBadgeColor = resp.sentiment === 'positive' ? C.green : resp.sentiment === 'negative' ? C.red : C.muted;

      const excerpt = (resp.response_text || '').slice(0, 320) + ((resp.response_text || '').length > 320 ? '…' : '');
      const excerptLines = doc.splitTextToSize(excerpt, CW - 10);
      const cardH = Math.min(excerptLines.length * 4.3 + 18, 48);

      doc.setFillColor(...C.card);
      doc.roundedRect(M, y, CW, cardH, 2, 2, 'F');
      doc.setDrawColor(...C.border);
      doc.setLineWidth(0.2);
      doc.roundedRect(M, y, CW, cardH, 2, 2, 'S');

      // Left accent by sentiment
      doc.setFillColor(...sentBadgeColor);
      doc.rect(M, y, 2, cardH, 'F');

      // Model badge
      const mbW = badge(doc, modelLabel, modelColor, M + 5, y + 3);

      // Sentiment badge
      badge(doc, resp.sentiment || 'neutral', sentBadgeColor, M + 5 + mbW + 2, y + 3);

      // Prompt text
      doc.setFontSize(7);
      doc.setTextColor(...C.dim);
      const promptSnip = (resp.prompt_text || '').slice(0, 80) + ((resp.prompt_text || '').length > 80 ? '…' : '');
      doc.text(`"${promptSnip}"`, M + 5, y + 13);

      // Response excerpt
      doc.setFontSize(7.5);
      doc.setTextColor(...C.muted);
      excerptLines.slice(0, Math.floor((cardH - 16) / 4.3)).forEach((line, li) => {
        doc.text(line, M + 5, y + 17 + li * 4.3);
      });

      y += cardH + 4;
    });
  }

  pageFooter(doc);
}

