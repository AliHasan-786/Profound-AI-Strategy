export async function generateAuditPDF(results) {
  const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });

  page1(doc, results);
  page2(doc, results);
  page3(doc, results);
  page4(doc, results);

  const brand    = (results.run?.brand_name || 'Brand').replace(/\s+/g, '_');
  const dateStr  = new Date().toISOString().slice(0, 10);
  doc.save(`${brand}_AI_Visibility_Audit_${dateStr}.pdf`);
}
